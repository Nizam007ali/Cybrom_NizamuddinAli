#include<iostream>
#include<cmath>
using namespace std;

int main()
{
	
    cout<<max(5,9)<<endl;		//only two parameter show max value
    cout<<min(5,9)<<endl;		//only two parameter show min value
    
    //cmath library function
    cout<<sqrt(169)<<endl;	//13 squareroot
    cout<<round(2.4)<<endl;	//0.01-0.49 = 0, 0.5-0.99 = 1.
    cout<<log(10)<<endl;		//log 
    
}
