public class ArithmeticOperator {
    public static void main(String args[])  
    { 
        int a=25;  
        int b=6;  

        System.out.println(a+b);//31  
        System.out.println(a-b);//19  
        System.out.println(a*b);//150  
        System.out.println(a/b);//4 
        System.out.println(a%b);//1 
    }
}